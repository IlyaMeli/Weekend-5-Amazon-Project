let cardsContainer = document.querySelector(".carousel-content");
let carousel = document.querySelector(".carousel-content");
let rightClick = document.querySelector(".fas.fa-chevron-right");
let leftClick = document.querySelector(".fas.fa-chevron-left");

rightClick.addEventListener("click", () => {
  cardsContainer.scrollBy(carousel.offsetWidth, 0);
});
leftClick.addEventListener("click", () => {
  cardsContainer.scrollBy(-carousel.offsetWidth, 0);
});
let smallPic1 = document.querySelector(".pic-1");
